/**
 * Escapes HTML special characters to prevent XSS injection
 * when interpolating user data into raw HTML strings (e.g., PDF templates).
 */
export function escapeHtml(str: string): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
